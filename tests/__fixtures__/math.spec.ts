import { wrapBetweenBounds } from './math';

it.each<number[]>([
    // [2, 2, 1, 5, 4],
    // [2, 3, 1, 5, 5],
    [102, 4, 101, 105, 101],
    [102, 5, 101, 105, 102],
    [102, 6, 101, 105, 103],
    [102, 7, 101, 105, 104],
    [102, 8, 101, 105, 105],
    [102, 9, 101, 105, 101],
    [102, 10, 101, 105, 102],
    [102, 11, 101, 105, 103],
    [102, 12, 101, 105, 104],
    [102, 13, 101, 105, 105],
    [102, 14, 101, 105, 101],
    [102, 15, 101, 105, 102],
    [102, 16, 101, 105, 103],
    [102, 17, 101, 105, 104],
    [102, 18, 101, 105, 105],

    [102, -4, 101, 105, 103],
    [102, -5, 101, 105, 102],
    [102, -6, 101, 105, 101],
    [102, -7, 101, 105, 105],
    [102, -8, 101, 105, 104],
    [102, -9, 101, 105, 103],
    [102, -10, 101, 105, 102],
    [102, -11, 101, 105, 101],
    [102, -12, 101, 105, 105],
    [102, -13, 101, 105, 104],
    [102, -14, 101, 105, 103],
    [102, -15, 101, 105, 102],
    [102, -16, 101, 105, 101],
    [102, -17, 101, 105, 105],
    [102, -18, 101, 105, 104],
])(`wrapBetweenBounds(start=%s, offset=%s, lower=%s, upper=%s)`, (start, offset, lower, upper, expected) => {
    expect(wrapBetweenBounds(start, offset, lower, upper)).toBe(expected);
})